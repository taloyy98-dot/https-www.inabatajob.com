# ==========================
def generate_pdf(row):
    pdf = FPDF()
    pdf.add_page()

    # ‡πÇ‡∏´‡∏•‡∏î‡∏ü‡∏≠‡∏ô‡∏ï‡πå‡πÑ‡∏ó‡∏¢ (‡∏ï‡πâ‡∏≠‡∏á‡∏°‡∏µ‡πÑ‡∏ü‡∏•‡πå THSarabunNew.ttf ‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡πÄ‡∏î‡∏µ‡∏¢‡∏ß‡∏Å‡∏±‡∏ô)
    pdf.add_font("THSarabunNew", "", "THSarabunNew.ttf", uni=True)
    pdf.set_font("THSarabunNew", size=16)

    # ‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠
    pdf.cell(0, 10, "üìã ‡πÉ‡∏ö‡∏™‡∏±‡πà‡∏á‡∏á‡∏≤‡∏ô", ln=True, align="C")
    pdf.ln(10)

    # ‡πÄ‡∏ï‡∏¥‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å row
    for col in row.index:
        text = f"{col}: {row[col]}"
        pdf.multi_cell(w=190, h=8, txt=text)

    # ‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤ PDF ‡πÄ‡∏õ‡πá‡∏ô bytes (‡πÅ‡∏Å‡πâ bytearray error)
    pdf_bytes = pdf.output(dest="S")
    return bytes(pdf_bytes)


